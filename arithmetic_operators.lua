-- Arithmetic Operators

num1 = 4
num2 = 8

addiction = num1+ num2
print(addiction)

subtraction = num1 - num2
print(subtraction)

multiplication = num1 * num2
print(multiplication)

division = num1 / num2
print(division)

exponentation = num1 ^ num2
print(exponentation)

unary = -num1
print(unary)

module = num2 % num1
print(module)